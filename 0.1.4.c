int ratepassword(char pass[])
{ /// ino check kon
    int lower = 0, upper = 0, number = 0, len = 0;
    for (int i = 0; i < strlen(pass); i++)
    {
        if (pass[i] >= 'a' && pass[i] <= 'z')
        {
            lower = 1;
        }
        else if (pass[i] >= 'A' && pass[i] <= 'Z')
        {
            upper = 1;
        }
        else if (pass[i] >= '0' && pass[i] <= '9')
        {
            number = 1;
        }
        if (i >= 6)
        {
            len = 1;
        }
    }
    int sum;
    sum = lower + upper + number + len;
    return sum;
}

int checkemail(char email[])
{ /// ino check kon
    int first = 0, second = 0, third = 0, fourth = 0, fifth = 0;
    for (int i = 0; i < strlen(email); i++)
    {
        if (email[i] == '@')
        {
            second = 1;
            if (i != 0)
            {
                first = 1;
            }
            if (email[i + 1] != '.')
            {
                fourth = 1;
            }
            for (; i < strlen(email); i++)
            {
                if (email[i] == '.')
                {
                    third = 1;
                    if (i != strlen(email) - 1)
                    {
                        fifth = 1;
                        break;
                    }
                }
            }
            break;
        }
    }
    if (first + second + third + fourth == 4)
    {
        return 1;
    }
    else
    {
        return 0;
    }
}

