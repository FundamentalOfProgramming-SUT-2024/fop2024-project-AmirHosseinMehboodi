int get_random(int min, int max)
{
    int random = (rand() % (max - min + 1)) + min;
    return random;
}

int find(int k, int rooms[], int numroom)
{
    for (int i = 0; i < numroom; i++)
    {
        if (rooms[i] == k)
        {
            return i;
        }
    }
    return -1;
}

