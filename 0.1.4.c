int checkpassword(char user[], char password[])
{ /// ino check kon
    char data[2][100];

    strcpy(data[0], user);
    strcpy(data[1], password);
    FILE *fptr;
    int check = 0;

    char file_name[100];
    snprintf(file_name, 100, "%s.txt", data[0]);
    fptr = fopen(file_name, "r");
    if (fptr == NULL)
    {
        return 0;
    }
    char temp[100];
    char temp1[100];
    fgets(temp, 100, fptr);
    temp[strlen(temp) - 1] = '\0';
    fgets(temp1, 100, fptr);
    temp1[strlen(temp1) - 1] = '\0';
    if (strcmp(data[0], temp) == 0)
    {
        check++;
    }
    if (strcmp(data[1], temp1) == 0)
    {
        check++;
    }
    mvprintw(1, 1, "%s %s %s %s", temp, data[0], temp1, data[1]);
    fclose(fptr);
    return check;
}

