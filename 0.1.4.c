int generate_walls(int x, int y, int a, int b)
{
    for (int i = 1; i <= a; i++)
    {
        mvprintw(y, i + x, "_");
        mvprintw(y + b + 1, i + x, "_");
    }
    for (int j = 1; j <= b + 1; j++)
    {
        mvprintw(y + j, x, "|");
        mvprintw(y + j, x + a + 1, "|");
    }
    refresh();
}

