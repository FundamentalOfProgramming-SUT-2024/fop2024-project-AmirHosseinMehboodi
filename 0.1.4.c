int boarder_big()
{
    mvaddch(0, 0, ACS_ULCORNER);
    mvaddch(0, 124, ACS_URCORNER);
    mvaddch(34, 0, ACS_LLCORNER);
    mvaddch(34, 124, ACS_LRCORNER);
    for (int i = 1; i < 124; i++)
    {
        mvaddch(0, i, ACS_HLINE);
        mvaddch(34, i, ACS_HLINE);
    }
    for (int i = 1; i < 34; i++)
    {
        mvaddch(i, 0, ACS_VLINE);
        mvaddch(i, 124, ACS_VLINE);
    }
}

